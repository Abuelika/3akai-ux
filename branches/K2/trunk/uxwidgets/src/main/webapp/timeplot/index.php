<p class="help-text">
This is a tool developed primarily for Plant Scientists designed to aid their learning
of plant evolution.  It is particularly useful for setting physiological and 
behavioural developments against physical parameters and changes in the environment.  
<br/><br/>
The tool will be introduced to students during the Part IA and Part II lecture course, but 
might also be useful for supervision and revision purposes.  There are some questions listed 
on this site that might guide interpretation and use of the tool.
<br/><br/>
Before you start, you might like to read the <a href="./help.php">help</a> section which details the full capabilities
